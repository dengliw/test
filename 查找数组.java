/*题目描述
在一个二维数组中，每一行都按照从左到右递增的顺序排序，
每一列都按照从上到下递增的顺序排序。请完成一个函数，
输入这样的一个二维数组和一个整数，判断数组中是否含有该整数。*/
public class Solution {
    public boolean Find(int target, int [][] array) {
		int n = target;
        int i,j;
        for( i = 0; i < array[0].length; i++){
            for( j = 0 ; j < array.length; j++){
                if( target == array[i][j] ){
                    return true;
                }
            }
        }
        return false;
    }
}